package Day2;

public class Operators {
	
	public static void main(String[] args) {
		int a = 20;
		int b = 60;
		int c = a+b;
		System.out.println(c);
		
		//Sub
		
		int a1 =50;
		int b1 = 70;
		int c1 = a1-b1;
		System.out.println(c1);
		
		//Multiplication
		int a2 = 40;
		int b2 = 40;
		int c2 = a2*b2;
		System.out.println(c2);
		
		//Division
		int a3 = 50;
		int b3 = 100;
		int c3 = b3/a3;
		System.out.println(c3);
		
		//modules
		
		int a4 = 20;
		int b4 = 40;
		int c4 = a4%b4;
		System.out.println(c4);
	}
	}

